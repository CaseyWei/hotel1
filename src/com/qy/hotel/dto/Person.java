package com.qy.hotel.dto;

public class Person {
	public String uname;
	public int upwd;
	public String sex;
	public String tele;
	public String addr;
	public String email;
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public int getUpwd() {
		return upwd;
	}
	public void setUpwd(int upwd) {
		this.upwd = upwd;
	}
	public String getSet() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getTele() {
		return tele;
	}
	public void setTele(String tele) {
		this.tele = tele;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
}
